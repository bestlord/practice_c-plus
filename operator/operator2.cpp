#include <iostream>
using namespace std;
int main() 
{ 
    int A = 12 , B = 8 ;
    cout << " A++ = " << A++ << endl;
    cout << " A = " << A << endl;
    cout << " ++B = " << ++B << endl;
    cout << " A-- = " << A-- << endl;
    cout << " A = " << A << endl;
    cout << " --B = " << --B << endl;
    cout << " B = " << B << endl;


    cout << " -A = " << -A << endl;
    cout << " +B = " << +B << endl;

    return 0 ;


}